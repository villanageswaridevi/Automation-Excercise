"""
Bitwise operators
-->Bitwise operators are used to compare (binary) numbers
-->They are
And (&), Or(|), Xor(^), Not(~), Zero fill left shift (<<), Signed right shift(>>)

"""
print(4 & 2)
print(2 & 3)

print(6 | 3)
print(8 | 3)

print(3 ^ 2)
print(4 ^ 5)

print(~2)

print(3 << 4)


















