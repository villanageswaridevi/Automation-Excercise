"""
Logical operators
-->Logical operators are used to combine conditional statements
-->They are and, or, not
"""
x = 5
print(x > 4 and x > 4) # Returns True if both statements are true
print(x > 6 or x < 2) # Returns True if one of the statements is true
print(not(x > 4 and x > 4)) # Reverse the result, returns False if the result is true
print(not(x > 6 or x < 2))








