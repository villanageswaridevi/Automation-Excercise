# Arthemetic operators
"""
Addition        symbol: +   ex: (x+y)

subtraction     symbol: -   ex: (x-y)

Multiplication  symbol: *  ex: (x*y)

Division        symbol: /  ex: (x/y)

Modulus         symbol: %  ex: (x%y)

Exponentiation  symbol: ** ex: (x**y)

Floor division  symbol: // ex: (x//y)

"""

# Example program

x = 10
y = 3
print(x+y) # add the values
print(x-y) # subtract the given values
print(x*y) # multiply the values
print(x/y) # divide the values
print(x%y) # gives the remainder
print(x**y) # raises the left number to the power of the right
print(x//y) # returns the integer part after division










