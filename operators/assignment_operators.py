"""
Assignment operators
-->Assignment operators are used to assign values to variables
-->There are =, +=, -=, *=, /=, %=, //=, **=
"""

x = 10
print(x)
x += 3
print(x)

y = 20
y -= 2
print(y)

x = 10
x *= 2
print(x)

y = 2
y /= 2
print(y)

a = 11
a %= 5
print(a)

b = 20
b //= 2
print(b)

c = 2
c **= 2
print(c)





















