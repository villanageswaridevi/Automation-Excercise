"""
Comparision operators
-->Comparison operators are used to compare two values
-->They are
==     Equal to
!=     Not equal
>     Greater than
<     Less than
>=    Greater than or equal to
<=    Less than or equal to
"""

x = 2
y = 4
print(x==y)
print(x!=y)
print(x>y)
print(x<y)
print(x>=y)
print(x<=y)














